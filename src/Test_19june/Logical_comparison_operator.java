package Test_19june;

public class Logical_comparison_operator {
    public static void main(String[] args) {
        int a = 10;
        int b = 5;

        System.out.printf("a==b: %b\n", a==b);
        System.out.printf("a>b: %b\n",a>b);
        System.out.printf("a < b: %b\n",a < b);
        System.out.printf("a > b && a>0: %b\n",a > b && a>0);
        System.out.printf("a > b || a>0: %b\n",a > b || a>0);
        System.out.printf("!(a>b): %b\n",!(a>b));


        }
    }


