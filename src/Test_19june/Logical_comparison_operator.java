package Test_19june;

public class Logical_comparison_operator {
    public static void main(String[] args) {
        int a = 10;
        int b = 20;

        System.out.println(a==b);
        System.out.println(a>b);
        System.out.println(a < b);
        System.out.println(a > b && a>0);
        System.out.println(a > b || a>0);
        System.out.println(!(a>b));


        }
    }


