package ex_10_For_Loop;

public class Lab103_For_If_Loop {
    public static void main(String[] args) {
        for(int shruti=0;shruti<18;shruti++){
            if(shruti<15){
                System.out.println("No gift only candy and hugs");
            }else{
                System.out.println("Got iphone as a gift from her father");
            }
        }
    }
}
